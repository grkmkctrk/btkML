import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

veriler = pd.read_csv('/content/maaslar.csv')

X = veriler.iloc[:, [1]]
Y = veriler.iloc[:, [2]]
# NUMPY ARRAY DIZI DONUSUMU
x = X.values
y = Y.values

from sklearn.tree import DecisionTreeRegressor

dT = DecisionTreeRegressor(random_state = 0)

dT.fit(x,y)

plt.scatter(x,y, c = 'r')
plt.plot(x, dT.predict(x), c = 'b')
plt.show()

dT.predict([[11]])

from sklearn.ensemble import RandomForestRegressor

rfr = RandomForestRegressor(random_state = 0, n_estimators=10)
rfr.fit(x, y)

plt.scatter(x, y, c = 'c')
plt.plot(x, rfr.predict(x), c = 'b')
plt.show()

rfr.predict([[11]])

"""farki gorebiliriz"""

tempX = x - 0.5

plt.plot(x, rfr.predict(tempX), c = 'r')
plt.plot(x, dT.predict(tempX), c = 'b')
plt.show()

